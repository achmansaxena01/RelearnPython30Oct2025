# range function
r1 = range(5)  # range function with final value
print(r1)
for i in r1:
    print(i)

r2 = range(1, 10)  # range function with initial and final value
print(r2)
for i in r2:
    print(i)

r3 = range(1, 18, 3)  # range function with initial , final and step value
print(r3)
for i in r3:
    print(i)
